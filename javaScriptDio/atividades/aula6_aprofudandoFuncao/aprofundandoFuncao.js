//tipos de funções 

/* //função declarativas - possuem o uso mais comum, deve ser declarada usando a palavra reservada “function” seguida do nome da função, parênteses “()” e chaves “{}”
function funcao() {
    console.log('Mensagem de uma função declarativa');
}
funcao(); */





/* //função de expressão - São funções atribuídas à expressões. A nomeação das funções por expressão é opcional
/* //com nomeação
var funcao = function funcao() {
    console.log('Mensagem de uma função de expressão')
}
funcao(); */

/* //sem nomeação
var funcao = function() {
    console.log('Mensagem de uma função de expressão')
}
funcao(); */






/*  //arrow function - São funções de expressão de sintaxe curta. Arrow functions sempre serão anônimas, e portanto não podem ser nomeadas. deve ser declarada com parênteses "()", seguido de "=>" e depois chaves "{}"
var funcao = () => {
    console.log('Mensagem de uma Arrow function');
}
funcao(); */






/* //funções aritméticas - 
• Number() – para converter valores em números;
• Prompt() – para registrar entradas de usuário;
• Alert() – para mostrar mensagem ao usuário;
• Template Strings – para usar strings junto com expressões; */

